/* eslint-disable no-undef */

import ExternalLink from '~/components/ExternalLink.vue'

export default {
  name: 'APropos',
  components: {
    ExternalLink
  },
  head () {
    const title = 'À propos'
    return {
      title
    }
  },
  data: () => ({
    fullpage: null
  }),
  computed: {
    windowIsTooSmall () {
      const { windowWidth, windowHeight } = this
      return windowWidth < 992 || windowHeight < 750
    }
  },
  watch: {
    windowWidth: {
      handler: 'toggleFullpage'
    },
    windowHeight: {
      handler: 'toggleFullpage'
    }
  },
  mounted () {
    const jQuery = require('jquery')
    global.$ = jQuery
    global.jQuery = jQuery
    require('fullpage.js')
    if (!this.windowIsTooSmall) {
      this.initFullpage()
    }
  },
  beforeDestroy () {
    this.destroyFullpage()
  },
  methods: {
    async toggleFullpage () {
      await this.$nextTick()
      if (this.windowIsTooSmall) {
        this.destroyFullpage()
      } else {
        this.initFullpage()
      }
    },
    initFullpage () {
      if (this.fullpage !== null) {
        return
      }
      $(this.$el).fullpage()
      this.fullpage = $(this.$el).fullpage
    },
    destroyFullpage () {
      if (this.fullpage === null) {
        return
      }
      this.fullpage.destroy('all')
      this.fullpage = null
    }
  }
}
</script>

